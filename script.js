function globalSearch(q){
  q=(q||"").trim();
  var box=document.getElementById("gResults"); if(!box) return;
  if(!q){ box.innerHTML=""; box.classList.remove("show"); return; }
  var hits=PEOPLE.filter(function(p){return p.name.indexOf(q)>=0;}).slice(0,30);
  box.innerHTML = hits.length
    ? hits.map(function(p){
        var meta = p.role==="議員" ? (" ・ "+esc(distLabel(p))) : (" ・ "+esc(p.role));
        return '<div class="gr-item" role="button" tabindex="0" onclick="gPick(&#39;'+p.name+'&#39;,&#39;'+p.county+'&#39;)"><b>'+esc(p.name)+'</b> <span class="gr-meta">'+esc(p.county)+meta+'</span></div>';
      }).join("")
    : '<div class="gr-empty">查無「'+esc(q)+'」</div>';
  box.classList.add("show");
}
